  String _normalizeArabic(String text) {
    String normalized = text;
    // Remove diacritics
    normalized = normalized.replaceAll(RegExp(r'[\u064B-\u0652\u0670]'), '');
    // Normalize Alef (أ, إ, آ -> ا)
    normalized = normalized.replaceAll(RegExp(r'[أإآ]'), 'ا');
    // Normalize Teh Marbuta (ة -> ه)
    normalized = normalized.replaceAll(RegExp(r'ة'), 'ه');
    // Normalize Ya (ى -> ي)
    normalized = normalized.replaceAll(RegExp(r'ى'), 'ي');
    return normalized.toLowerCase().trim();
  }

  @override
  Future<Result> search({
    required String query,
    required String filter,
    required String scope,
  }) async {
    // Simulate API delay
    await Future.delayed(const Duration(milliseconds: 400));

    try {
      final String normalizedQuery = _normalizeArabic(query);
      final List<SearchModel> matchingResults = [];

      for (var item in _mockData) {
        // 1. Check Filter matching
        // If filter is not "الكل" (all), it must match the item's filter
        if (filter != 'الكل' && item['filter'] != filter) {
          continue;
        }

        // 2. Check Scope matching
        // Scopes: "الكل", "الخطب", "الرسائل", "الحكم"
        // In the mock data, dictionary/grammar/names have 'الكل' as category.
        // If scope is not "الكل" and item's category is not "الكل", they must match.
        // If item's category is 'الكل', it's allowed in any scope if query is matching.
        if (scope != 'الكل' && item['category'] != scope && item['category'] != 'الكل') {
          continue;
        }

        // 3. Check text matching
        if (normalizedQuery.isNotEmpty) {
          final String normalizedTitle = _normalizeArabic(item['title'] as String);
          final String normalizedContent = _normalizeArabic(item['content'] as String);

          if (!normalizedTitle.contains(normalizedQuery) &&
              !normalizedContent.contains(normalizedQuery)) {
            continue;
          }
        }

        matchingResults.add(SearchModel.fromJson(item));
      }

      return Success(code: 200, response: matchingResults);
    } catch (e) {
      return Failure(code: 500, response: e.toString());
    }
  }
